class FallbackComplianceAgent:
    """Ищет самые рискованные запрещенные формулировки без LLM."""

    async def analyze(self, transcript: Sequence[TranscriptSegment]) -> ComplianceResult:
        text = transcript_text(transcript)
        issues = [
            ComplianceIssue(
                severity=severity,
                phrase=phrase,
                description=description,
                recommendation=recommendation,
            )
            for phrase, (severity, description, recommendation) in FORBIDDEN_PHRASES.items()
            if phrase in text
        ]
        return ComplianceResult(passed=not issues, issues=issues)
